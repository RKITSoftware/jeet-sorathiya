-- Union
SELECT P01F01, P01F02
FROM emp01
UNION
SELECT P02F01,P02F02
FROM emp02 ;

-- Union All
SELECT P01F01, P01F02
FROM emp01
UNION ALL
SELECT P02F01,P02F02
FROM emp02;

-- ORDER BY AND LIMIT
(SELECT P01F01, P01F02
FROM emp01 ORDER BY P01F03 limit 1)
UNION
(SELECT P02F01,P02F02
FROM emp02)
ORDER BY P01F01
LIMIT 1;

